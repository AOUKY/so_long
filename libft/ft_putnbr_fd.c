#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long	n;
	char	c[12];
	int		i;

	n = nb;
	i = 0;
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n == 0)
		c[i++] = '0';
	while (n > 0)
	{
		c[i] = n % 10 + 48;
		n = n / 10;
		i++;
	}
	i--;
	while (i >= 0)
	{
		ft_putchar_fd(c[i], fd);
		i--;
	}
}
